package com.leetcode.easy.extand;

/**
 * @author nathan
 * @date 2020/8/5 23:53
 * @desc ListNode
 */
public class ListNode {

    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
